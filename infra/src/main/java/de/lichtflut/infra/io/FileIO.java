package de.lichtflut.infra.io;

import java.io.File;

/**
 * Helper class for IO operations in file system.
 * 
 * Created: 06.10.2009
 * 
 * @author Oliver Tigges
 */
public class FileIO {

	/**
	 * Delete the directory recursive.
	 * @param path The path of the directory.
	 * @return true if the directory could have been deleted.
	 */
	static public boolean deleteDirectory(final File path) {
		if (path.exists()) {
			File[] files = path.listFiles();
			for (int i = 0; i < files.length; i++) {
				if (files[i].isDirectory()) {
					deleteDirectory(files[i]);
				} else {
					files[i].delete();
				}
			}
		}
		return (path.delete());
	}

}
